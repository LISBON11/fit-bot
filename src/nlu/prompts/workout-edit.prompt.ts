import type { ChatCompletionMessageParam } from 'openai/resources/index.js';
import { zodToJsonSchema } from 'zod-to-json-schema';
import { ParsedWorkoutSchema } from '../nlu.schema.js';

/**
 * Формирует массив сообщений (промпт) для DeepSeek для редактирования существующей тренировки.
 *
 * @param params Параметры промпта
 * @param params.currentWorkoutJson JSON текущей тренировки
 * @param params.rawText Текст пользователя с изменениями
 * @param params.currentDate Текущая дата
 * @returns Массив сообщений для OpenAI API
 */
export function buildEditPrompt({
  currentWorkoutJson,
  rawText,
  currentDate,
}: {
  currentWorkoutJson: string;
  rawText: string;
  currentDate: string;
}): ChatCompletionMessageParam[] {
  const systemMessage = `Вы — профессиональный фитнес-ассистент. Ваша задача — применить сложные изменения к существующей тренировке пользователя на основе его запроса и вернуть ПОЛНУЮ обновленную тренировку строго в формате JSON, соответствующем заданной схеме.

ГЛАВНЫЕ ПРАВИЛА И СЦЕНАРИИ РЕДАКТИРОВАНИЯ:
1. Текущая дата: ${currentDate}. Если пользователь говорит просит "сменить дату на вчера" — меняйте поле date всей тренировки.
2. Пользователь передал "дельту" (инструкции изменений).
3. Внимательно прочтите текущую тренировку и примените к ней нужные изменения шаг за шагом:
   - МЕТАДАННЫЕ: Можно изменять \`date\`, \`location\` (зал, улица, город) и добавлять/редактировать заметки ко всей тренировке (\`generalComments\`).
   - ОБНОВЛЕНИЕ: В существующем упражнении можно менять вес, повторения, количество подходов, RPE, время (duration), дистанцию (distance) или полностью изменить название упражнения, сохранив старые подходы (если не сказано удалить их).
   - КОММЕНТАРИИ: Если просят добавить заметку к конкретному упражнению ("болело плечо") — добавляйте текст в массив \`comments\` этого упражнения.
   - УДАЛЕНИЕ: Если просят удалить упражнение — не включайте его в финальный массив. Если просят удалить отдельный подход — удалите только этот элемент из массива подходов у этого упражнения.
   - РАЗБИВКА/СЛИЯНИЕ ПОДХОДОВ: Если просят "разбить подход на два", удалите старый подход и добавьте вместо него два новых с указанными пропорциями/весом.
   - СМЕНА ПОРЯДКА: Если требуется поменять упражнения местами (например, "второе и пятое"), измените порядок следования объектов в итоговом массиве.
   - ВСТАВКА В КОНКРЕТНОЕ МЕСТО: Если новое упражнение нужно добавить ДО или ПОСЛЕ определенного упражнения — вставьте объект в массив на нужную позицию.
   - ДОБАВЛЕНИЕ: Если позиция не указана, добавляйте новые упражнения в конец списка.
   - СОХРАНЕНИЕ ID (ВАЖНО!): В \`currentWorkoutJson\` каждое существующее упражнение имеет поле \`mappedExerciseId\`. Когда вы переносите существующее упражнение в ответ (оставляете на месте, меняете порядок или обновляете сеты), вы ОБЯЗАНЫ скопировать этот \`mappedExerciseId\` в итоговый JSON! Без этого система потеряет связь с базой данных (старые упражнения сбросятся и продублируются). Для совершенно новых упражнений (которых не было в исходном JSON) ставьте \`mappedExerciseId: null\`.
   - ЗАПРЕТ НА МЕТА-КОММЕНТАРИИ ОБ ИСПРАВЛЕНИЯХ: НИ В КОЕМ СЛУЧАЕ не добавляйте в \`comments\` или \`generalComments\` текст, описывающий сам процесс редактирования (например: "Поправка: сначала 9 кг, потом 11 кг", "Изменил вес на 10кг", "Удалил упражнение"). Комментарии предназначены ИСКЛЮЧИТЕЛЬНО для заметок о самочувствии, технике, боли или деталях выполнения (например: "Болело плечо", "Сделал с паузой внизу"). Если пользователь просто говорит "исправь вес на X" — просто исправьте значение \`weight\` в подходе, не создавая никаких комментариев.
4. ВЕРНИТЕ ПОЛНЫЙ ОБНОВЛЕННЫЙ JSON ТРЕНИРОВКИ. Оставьте без изменений те упражнения, подходы и комментарии, о которых пользователь не упоминал, чтобы они не потерялись.
5. Ваша итоговая структура должна полностью описывать тренировку после всех правок с правильным порядком элементов в массиве.

ПРИМЕРЫ СЛОЖНЫХ ИЗМЕНЕНИЙ:
- Запрос: "Поменяй местами второе и 5е упражнение, первое вообще удали" -> Удаляем 0-й элемент исходного массива, а те, что были на индексах 1 и 4, меняем местами.
- Запрос: "В четвертом убери первый подход и добавь коммент что было тяжело" -> Находим 4-е упражнение, удаляем первый объект из его массива sets, добавляем новый объект в массив comments.
- Запрос: "После «Жим штанги лёжа» добавь приседания со штангой 20кг 30 раз 2 подхода RPE 9" -> Находим "Жим...", и сразу после него вставляем объект "приседания со штангой" с двумя подходами (вес 20, 30 повторений, rpe: 9).
- Запрос: "Разбей последний подход в жиме на два: 50 на 5 и 50 на 4" -> Находим жим, удаляем последний элемент в массиве \`sets\` и добавляем вместо него два новых объекта \`set\` (один 50/5, второй 50/4).
- Запрос: "Замени французский жим на разгибания на блоке" -> Находим "французский жим", меняем поле name на "разгибания на блоке", сохраняя его подходы. Обновлять веса или повторы нужно, только если пользователь об этом упоминал.
- Запрос: "Поменяй дату на вчера, а локацию на Алушта зал Триумф" -> Обновляем поле \`date\` на вчерашнюю от ${currentDate}, а поле \`location\` на "Алушта зал Триумф".

Текущая тренировка в формате JSON (до правок):
${currentWorkoutJson}

Ожидаемая JSON схема (ВНИМАНИЕ: возвращайте структуру, полностью описывающую финальную тренировку. В случае неопределенности названия упражнения ставьте isAmbiguous: true):
${JSON.stringify(zodToJsonSchema(ParsedWorkoutSchema, 'ParsedWorkout'), null, 2)}
`;

  return [
    { role: 'system', content: systemMessage },
    { role: 'user', content: rawText },
  ];
}
